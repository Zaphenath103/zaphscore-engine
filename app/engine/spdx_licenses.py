def lookup_spdx_license(identifier: str) -> dict | None:
    """Look up a license by SPDX identifier (case-insensitive fallback)."""
    if identifier in SPDX_LICENSES:
        return SPDX_LICENSES[identifier]
    upper = identifier.upper()
    for key, val in SPDX_LICENSES.items():
        if key.upper() == upper:
            return val
    return None


def get_spdx_category(identifier: str) -> str:
    """Return category for an SPDX identifier (returns "unknown" if not found)."""
    meta = lookup_spdx_license(identifier)
    return meta["category"] if meta else "unknown"


def is_osi_approved(identifier: str) -> bool:
    """Return True if the license is OSI-approved."""
    meta = lookup_spdx_license(identifier)
    return bool(meta and meta.get("osi"))


def is_fsf_approved(identifier: str) -> bool:
    """Return True if the license is FSF-approved free software."""
    meta = lookup_spdx_license(identifier)
    return bool(meta and meta.get("fsf"))


def list_by_category(category: str) -> list[str]:
    """Return all SPDX identifiers belonging to a given category."""
    return [k for k, v in SPDX_LICENSES.items() if v["category"] == category]
